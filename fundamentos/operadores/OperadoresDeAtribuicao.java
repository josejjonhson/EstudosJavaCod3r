package fundamentos.operadores;

public class OperadoresDeAtribuicao {

	public static void main(String[] args) {
		
		int a = 3;
		int b = a;
		int c = a + b;
		int d = 0;
		d += b;
		
		System.out.println(a + "" + c + "" + d);
		
		/**
		 * Formas reduzidas:
		 * +=
		 * -+
		 * /=
		 * *=
		 * %=
		 */
		
		
	}
}
