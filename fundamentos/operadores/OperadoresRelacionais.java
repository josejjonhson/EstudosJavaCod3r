package fundamentos.operadores;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		// Comportamento estranho que acontece no Java
		int a = 97;
		int b = 'a';
		System.out.println(a == b);
		System.out.println('\u0061');
		
		/**
		 * Operadores:
		 * >
		 * <
		 * >=
		 * <= 
		 * !=
		 */
	}
}
